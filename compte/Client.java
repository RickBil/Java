public class Client {
    // Attributs
    private int num;
    private String nom;
    private String prenom;

    // Methodes

    public int getNum() {
        return num;
    }
    public String getPrenom() {
        return prenom;
    }
    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }
    public String getNom() {
        return nom;
    }
    public void setNom(String nom) {
        this.nom = nom;
    }
    public void setNum(int num) {
        this.num = num;
    }
}
